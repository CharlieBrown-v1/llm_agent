        args.output_dir = lumos_dir_path.joinpath(f'results/train/{TASK}/{task_info}')

        data_files = {}
        dataset_args = {}
        if args.train_file is not None:
            data_files["train"] = str(args.train_file)
        raw_datasets = load_dataset(
            "json",
            data_files=data_files,
            **dataset_args,
        )

        tokenizer = AutoTokenizer.from_pretrained(args.model_name_or_path, use_fast=not args.use_slow_tokenizer)

        # Preprocessing the datasets.
        if "prompt" in raw_datasets["train"].column_names and "completion" in raw_datasets["train"].column_names:
            encode_function = partial(
                encode_with_prompt_completion_format,
                tokenizer=tokenizer,
                max_seq_length=args.max_seq_length,
            )
        elif "messages" in raw_datasets["train"].column_names:
            encode_function = partial(
                encode_with_messages_format,
                tokenizer=tokenizer,
                max_seq_length=args.max_seq_length,
            )
        else:
            raise ValueError("You need to have either 'prompt'&'completion' or 'messages' in your column names.")

        accelerator_log_kwargs = {}
        if args.with_tracking:
            accelerator_log_kwargs["log_with"] = args.report_to
            accelerator_log_kwargs["project_dir"] = args.output_dir
        accelerator = Accelerator(gradient_accumulation_steps=args.gradient_accumulation_steps, **accelerator_log_kwargs)
        with accelerator.main_process_first():
            tmp_datasets = raw_datasets.map(
                encode_function,
                batched=False,
                num_proc=args.preprocessing_num_workers,
                load_from_cache_file=not args.overwrite_cache,
                remove_columns=[name for name in raw_datasets["train"].column_names if name not in ["input_ids", "labels", "attention_mask"]],
                desc="Tokenizing and reformatting instruction data",
            )
            tmp_datasets.set_format(type="pt")
            lm_datasets = tmp_datasets.filter(lambda example: (example['labels'] != -100).any())

        train_dataset = lm_datasets["train"]

        ds_dict[module] = train_dataset

